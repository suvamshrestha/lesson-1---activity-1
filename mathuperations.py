import math
print('the floor and ceiling value of 23.56 are '+ str(math.ceil(23.56)) + ' and ' + str(math.floor(23.56) ) + ' respectively')
x = 10
y = -15
print('the value of x after copying is ' + str(math.copysign(x, y)))
print('the absolute value of -96 and -56 are ' + str(math.fabs(-96)) + ' and ' + str(math.fabs(-56)) + ' respectively')
print('the gcd of 96 and 56 is ' + str(math.gcd(-96, -56)))